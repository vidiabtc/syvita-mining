<h2>What are CityCoins?</h2>
<p><a href="https://docs.citycoins.co/about-citycoins/what-are-citycoins">CityCoins</a> are cryptocurrencies that give communities the power to improve and program their cities. CityCoins are powered by <a href="https://www.stacks.co/">Stacks</a>, a protocol that enables smart contracts on the Bitcoin network. CityCoins are launched by the community, one city at a time. Each city’s coin has zero pre-mine, zero team allocation, and zero VC investment before mining the coin goes live.</p> <p>This means a level playing field for mining the CityCoin of your choice. Mining directly supports the city by funding their treasury wallet with STX, which the city can then use, sell, or stack to earn native BTC yield. Miami Coin, the first CityCoin to launch, is already responsible for <a href="https://www.forbes.com/sites/lisakim/2021/11/11/miami-mayor-residents-will-get-digital-wallets-for-bitcoin-dividend/?sh=29cdb92850c8">Mayor Suarez’s</a> plans to pay every single Miami resident a basic income in native BTC!</p>
